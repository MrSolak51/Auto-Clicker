CREATE DEFINER=`root`@`localhost` PROCEDURE `get_yearly_statics`(
    IN user_idP INT,
    IN yearP INT
)
BEGIN
    DECLARE month_ INT DEFAULT 1;
    DECLARE earnings FLOAT DEFAULT 0;
    DECLARE expenses FLOAT DEFAULT 0;
    DECLARE total FLOAT DEFAULT 0;
    DECLARE yearly_earnings FLOAT DEFAULT 0;
    DECLARE yearly_expenses FLOAT DEFAULT 0;
    DECLARE yearly_total FLOAT DEFAULT 0;

    -- Döngü başı: 1'den 12'ye kadar her ayı işle
    my_loop: LOOP
        -- Koşul kontrolü (12'yi geçtiğinde döngüden çık)
        IF month_ > 12 THEN
            LEAVE my_loop;
        END IF;

        -- Verileri sorgula: her ayın toplamını al
        SELECT 
            IFNULL(SUM(total_earnings), 0), 
            IFNULL(SUM(total_expenses), 0), 
            IFNULL(SUM(total), 0)
        INTO earnings, expenses, total
        FROM statics
        WHERE user_id = user_idP AND MONTH(date_) = month_ AND YEAR(date_) = yearP;

        -- Yıllık toplamları hesapla
        SET yearly_earnings = yearly_earnings + earnings;
        SET yearly_expenses = yearly_expenses + expenses;
        SET yearly_total = yearly_total + total;

        -- Ay sonrasında month_'u bir artır
        SET month_ = month_ + 1;
    END LOOP my_loop;

    -- Yıllık toplamları döndür
    SELECT 
        yearly_earnings AS total_earnings, 
        yearly_expenses AS total_expenses, 
        yearly_total AS total;
END ;;
